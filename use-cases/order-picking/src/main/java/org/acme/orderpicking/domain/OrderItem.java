package org.acme.orderpicking.domain;

public class OrderItem {

    private String id;
    private Order order;
    private String product;
    private int volume;

    public OrderItem() {
        //marshalling constructor
    }

    public OrderItem(String id, Order order, String product, int volume) {
        this.id = id;
        this.order = order;
        this.product = product;
        this.volume = volume;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public Order getOrder() {
        return order;
    }

    public void setOrder(Order order) {
        this.order = order;
    }

    public String getProduct() {
        return product;
    }

    public void setProduct(String product) {
        this.product = product;
    }

    public int getVolume() {
        return volume;
    }

    public void setVolume(int volume) {
        this.volume = volume;
    }

    @Override
    public String toString() {
        return "OrderItem{" +
                "id='" + id + '\'' +
                ", order=" + order +
                ", product=" + product +
                ", volume=" + volume +
                '}';
    }
}
